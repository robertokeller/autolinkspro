#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const cwd = process.cwd();
const composePath = path.join(cwd, "docker-compose.coolify.yml");
const envExamplePath = path.join(cwd, ".env.coolify.example");
const outputPath = path.join(cwd, "docs", "DEPLOY_AUTOGENERATED.md");

function parseCompose(composeContent) {
  const lines = composeContent.split(/\r?\n/g);
  const variableMap = new Map();
  const serviceMap = new Map();

  let inServicesBlock = false;
  let currentService = null;

  for (const line of lines) {
    const trimmed = line.trim();

    if (!inServicesBlock) {
      if (trimmed === "services:") inServicesBlock = true;
      continue;
    }

    if (/^[A-Za-z0-9_-]+:\s*$/.test(line) && !line.startsWith("  ")) {
      break;
    }

    const serviceMatch = line.match(/^  ([a-z0-9-]+):\s*$/);
    if (serviceMatch) {
      currentService = serviceMatch[1];
      if (!serviceMap.has(currentService)) {
        serviceMap.set(currentService, {
          name: currentService,
          memory: "-",
          cpus: "-",
          healthcheck: "-",
        });
      }
      continue;
    }

    if (!currentService) continue;

    const serviceInfo = serviceMap.get(currentService);
    const memoryMatch = line.match(/^\s+memory:\s*(.+)\s*$/);
    if (memoryMatch) {
      serviceInfo.memory = memoryMatch[1].trim();
    }

    const cpuMatch = line.match(/^\s+cpus:\s*'?(.*?)'?\s*$/);
    if (cpuMatch) {
      serviceInfo.cpus = cpuMatch[1].trim();
    }

    const healthMatch = line.match(/fetch\('([^']+)'/);
    if (healthMatch) {
      serviceInfo.healthcheck = healthMatch[1];
    }

    const referenceMatches = [...line.matchAll(/\$\{([^}]+)\}/g)];
    for (const refMatch of referenceMatches) {
      const parsedRef = parseVarReference(refMatch[1]);
      if (!parsedRef) continue;

      if (!variableMap.has(parsedRef.name)) {
        variableMap.set(parsedRef.name, {
          name: parsedRef.name,
          required: false,
          defaults: new Set(),
          services: new Set(),
        });
      }

      const current = variableMap.get(parsedRef.name);
      current.required = current.required || parsedRef.required;
      if (parsedRef.defaultValue !== null && parsedRef.defaultValue !== "") {
        current.defaults.add(parsedRef.defaultValue);
      }
      current.services.add(currentService);
    }
  }

  return { variableMap, serviceMap };
}

function parseVarReference(rawReference) {
  const requiredMatch = rawReference.match(/^([A-Z0-9_]+):\?(.*)$/);
  if (requiredMatch) {
    return {
      name: requiredMatch[1],
      required: true,
      defaultValue: null,
    };
  }

  const defaultMatch = rawReference.match(/^([A-Z0-9_]+):-([\s\S]*)$/);
  if (defaultMatch) {
    return {
      name: defaultMatch[1],
      required: false,
      defaultValue: defaultMatch[2].trim(),
    };
  }

  const plainMatch = rawReference.match(/^([A-Z0-9_]+)$/);
  if (plainMatch) {
    return {
      name: plainMatch[1],
      required: false,
      defaultValue: null,
    };
  }

  return null;
}

function parseEnvExample(envContent) {
  const envMap = new Map();
  const lines = envContent.split(/\r?\n/g);

  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;

    const match = line.match(/^([A-Z0-9_]+)=(.*)$/);
    if (!match) continue;

    envMap.set(match[1], match[2].trim());
  }

  return envMap;
}

function escapeTableCell(value) {
  return String(value || "-").replace(/\|/g, "\\|");
}

function formatVar(varName) {
  return `\`${varName}\``;
}

function buildVariablesTableRows(variables, envExamples) {
  return variables
    .map((variable) => {
      const services = [...variable.services].sort().join(", ") || "-";
      const defaultValue = [...variable.defaults].sort().join(" | ") || "-";
      const sample = envExamples.get(variable.name) || "-";
      return `| ${formatVar(variable.name)} | ${escapeTableCell(services)} | ${escapeTableCell(defaultValue)} | ${escapeTableCell(sample)} |`;
    })
    .join("\n");
}

function buildServiceTableRows(serviceMap) {
  return [...serviceMap.values()]
    .sort((a, b) => a.name.localeCompare(b.name))
    .map((service) => {
      return `| \`${service.name}\` | ${escapeTableCell(service.memory)} | ${escapeTableCell(service.cpus)} | ${escapeTableCell(service.healthcheck)} |`;
    })
    .join("\n");
}

function main() {
  const composeContent = readFileSync(composePath, "utf8");
  const envExampleContent = readFileSync(envExamplePath, "utf8");
  const { variableMap, serviceMap } = parseCompose(composeContent);
  const envExamples = parseEnvExample(envExampleContent);

  const allVariables = [...variableMap.values()].sort((a, b) => a.name.localeCompare(b.name));
  const requiredVariables = allVariables.filter((variable) => variable.required);
  const optionalVariables = allVariables.filter((variable) => !variable.required);
  const generatedAt = new Date().toISOString();

  const markdown = `# Deploy Autogerado (Coolify)

> Arquivo gerado automaticamente por \`scripts/generate-deploy-doc.mjs\`.  
> Atualizado em: \`${generatedAt}\`

## Fluxo automático recomendado

\`\`\`bash
npm run env:private:setup
npm run deploy:docs
npm run deploy:preflight
\`\`\`

Ou em comando único:

\`\`\`bash
npm run deploy:prepare
\`\`\`

## Regras de segurança para commit

- Nunca commitar \`.env\`, \`.env.local\`, \`.env.coolify\` e qualquer segredo.
- Use \`.private/env/\` para versões locais dos envs.
- Use \`.private/secrets/\` para segredos em arquivo local (ex.: salt local de criptografia).
- Rode \`npm run commit:safety\` antes de abrir PR.

## Variáveis obrigatórias no compose

| Variável | Serviços | Default no compose | Exemplo em .env.coolify.example |
| --- | --- | --- | --- |
${buildVariablesTableRows(requiredVariables, envExamples)}

## Variáveis opcionais no compose

| Variável | Serviços | Default no compose | Exemplo em .env.coolify.example |
| --- | --- | --- | --- |
${buildVariablesTableRows(optionalVariables, envExamples)}

## Capacidade inicial por serviço (baseline de escala)

| Serviço | Limite de memória | Limite de CPU | Healthcheck |
| --- | --- | --- | --- |
${buildServiceTableRows(serviceMap)}

## Próximos passos de escala (skill: scalability)

- Validar p95/p99 por serviço com carga sintética antes de campanha.
- Ajustar \`deploy.resources.limits\` conforme uso real de CPU/memória.
- Escalar horizontalmente serviços stateless primeiro (\`web\`, \`api\`, \`scheduler\`).
- Manter filas/scheduler idempotentes para evitar duplicidade em retry/redeploy.
`;

  writeFileSync(outputPath, markdown, "utf8");
  console.log(`[deploy-doc] arquivo atualizado: ${path.relative(cwd, outputPath)}`);
  console.log(`[deploy-doc] variáveis obrigatórias: ${requiredVariables.length}`);
  console.log(`[deploy-doc] variáveis opcionais: ${optionalVariables.length}`);
}

main();
