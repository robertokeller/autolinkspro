/**
 * scripts/extract-brasil-svg.mjs
 *
 * One-time script to bundle the mapa-brasil SVG paths and IBGE JSON data
 * into a TypeScript constant file used by the pure-React BrasilMap component.
 *
 * Run: node scripts/extract-brasil-svg.mjs
 */

import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

// ── Input files from mapa-brasil package ────────────────────────────────────
const SVG_PATH = resolve(
  root,
  "node_modules/mapa-brasil/data/svg/low/federacao/br_unidades_da_federacao.svg",
);
const JSON_PATH = resolve(
  root,
  "node_modules/mapa-brasil/data/json/federacao/br_unidades_da_federacao.json",
);

const OUTPUT_DIR = resolve(root, "src/data");
const OUTPUT_FILE = resolve(OUTPUT_DIR, "brasil-states.ts");

// ── Parse ────────────────────────────────────────────────────────────────────
const svgText = readFileSync(SVG_PATH, "utf-8");
const jsonData = JSON.parse(readFileSync(JSON_PATH, "utf-8"));

// Extract all <path d="..."/> elements in document order
const pathRegex = /<path\s+d="([^"]+)"\s*\/>/g;
const paths = [];
let match;
while ((match = pathRegex.exec(svgText)) !== null) {
  paths.push(match[1]);
}

if (paths.length !== jsonData.length) {
  console.error(
    `Mismatch: ${paths.length} SVG paths vs ${jsonData.length} JSON entries`,
  );
  process.exit(1);
}

// Build combined array sorted by IBGE code so lookup is predictable
const states = jsonData.map((entry, i) => ({
  ibge: Number(entry.CD_GEOCUF),
  name: entry.NM_ESTADO,
  region: entry.NM_REGIAO,
  path: paths[i],
}));

// ── Emit TypeScript ──────────────────────────────────────────────────────────
mkdirSync(OUTPUT_DIR, { recursive: true });

const tsContent = `// AUTO-GENERATED by scripts/extract-brasil-svg.mjs — do not edit manually.
// Source: node_modules/mapa-brasil/data/svg/low/federacao/ + /json/federacao/
// SVG viewBox: 0 0 800 691

export interface BrasilState {
  /** IBGE code (CD_GEOCUF) used to match density data from the analytics API */
  ibge: number;
  /** Full state name in Portuguese */
  name: string;
  /** Macro-region */
  region: string;
  /** SVG path data (d attribute) */
  path: string;
}

export const BRASIL_STATES: BrasilState[] = ${JSON.stringify(states, null, 2)};

/** Fast lookup: ibge → BrasilState */
export const BRASIL_STATE_BY_IBGE: Record<number, BrasilState> = Object.fromEntries(
  BRASIL_STATES.map((s) => [s.ibge, s]),
);
`;

writeFileSync(OUTPUT_FILE, tsContent, "utf-8");
console.log(`✓ Wrote ${states.length} states to ${OUTPUT_FILE}`);
